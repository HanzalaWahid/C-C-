// 1. For Loop

// Used when the number of iterations is known beforehand.

// for (initialization; condition; increment/decrement) {
//     // code to be executed
// }

#include <stdio.h>

int main() {
    for (int i = 1; i <= 5; i++) {
        printf("Number: %d\n", i);
    }
    return 0;
}


// Output:

// Number: 1
// Number: 2
// Number: 3
// Number: 4
// Number: 5