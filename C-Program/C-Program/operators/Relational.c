
#include <stdio.h>

int main() {
    int a = 5, b = 3;

    // addition
    printf("Addition: %d\n", a + b);

    // subtraction
    printf("Subtraction: %d\n", a - b);

    // multiplication
    printf("Multiplication: %d\n", a * b);

    // division
    printf("Division: %.2f\n", (float)a / b);

    // modulus (remainder)
    printf("Modulus: %d\n", a % b);

 
    return 0;
}

